    private void actualizarGUI() {
        double total = pController.getPreciosXCantidades();
        int cantidad = pController.getCantidadesListadas();
        double promedio = 0;

        if (cantidad != 0) {
            promedio = total / cantidad;
        } else {
            promedio = 0;
        }

        LblPromedio.setText(String.valueOf(promedio));
        LblSubTotal.setText(String.valueOf(total * 0.82));
        LblIGV.setText(String.valueOf(total * 0.18));
        LblTotal.setText(String.valueOf(total));

        txtArea.setText("Total de Productos: " + cantidad);
    }

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        int rpta = JOptionPane.showConfirmDialog(this, "Deseas Grabar el Registro", "Grabar", JOptionPane.YES_NO_OPTION);
        if (JOptionPane.NO_OPTION == rpta) {
            return;
        }

        if (pController.buscarIndex(Integer.parseInt(this.TxtCodigo.getText())) != -1) {
            JOptionPane.showMessageDialog(this, "Producto existente");
            return;
        }

        Producto obj = new Producto();
        obj.setCodigo(Integer.parseInt(this.TxtCodigo.getText()));
        obj.setNombre(this.TxtNombre.getText());
        obj.setPrecio(Double.parseDouble(this.TxtPrecio.getText()));
        obj.setStock(Integer.parseInt(this.TxtStock.getText()));

        //adicionar el elemento en la lista
        pController.add(obj);
        listar();
        actualizarGUI();
        btnNuevoActionPerformed(evt);
// TODO add your handling code here:
    }//GEN-LAST:event_btnGrabarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String[] options = {"Vaciar Lista", "Eliminar Elemento Seleccionado", "Cancelar"};
        int rpta = 0;
        rpta = JOptionPane.showOptionDialog(this, "Desea Vaciar o eliminar un elemento", "Eliminar", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);
        if (rpta >= 2) {
            return;
        }
        if (rpta == 1) {

            if (pController.buscarIndex(Integer.parseInt(this.TxtCodigo.getText())) == -1) {
                JOptionPane.showMessageDialog(this, "No ubicamos el producto");
                return;
            }

            while (pController.buscarIndex(Integer.parseInt(this.TxtCodigo.getText())) != -1) {
                pController.remove(pController.buscar(Integer.parseInt(this.TxtCodigo.getText())));
            }

        } else {
            pController.clear();
            dtm.setRowCount(0);
            this.tProducto.setModel(dtm);
        }
        listar();
        actualizarGUI();
        btnNuevoActionPerformed(evt);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int rpta = JOptionPane.showConfirmDialog(this, "Deseas Grabar la Edición", "Editar", JOptionPane.YES_NO_OPTION);
        if (JOptionPane.NO_OPTION == rpta) {
            return;
        }
        int index = pController.buscarIndex(Integer.parseInt(this.TxtCodigo.getText()));
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "No ubicamos el producto");
            return;
        }
        Producto obj = new Producto();
        obj.setCodigo(Integer.parseInt(this.TxtCodigo.getText()));
        obj.setNombre(this.TxtNombre.getText());
        obj.setPrecio(Double.parseDouble(this.TxtPrecio.getText()));
        obj.setStock(Integer.parseInt(this.TxtStock.getText()));

        //adicionar el elemento en la lista
        pController.set(index, obj);

        listar();
        actualizarGUI();
        btnNuevoActionPerformed(evt);
        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarActionPerformed

