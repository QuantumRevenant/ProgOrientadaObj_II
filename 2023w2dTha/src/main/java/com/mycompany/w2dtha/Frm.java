    private void listar() {
        if (pController.size() <= 0) {
            JOptionPane.showMessageDialog(this, "La tabla no tiene datos");
            return;
        }
        dtm.setRowCount(0);
        for (int i = 0; i < pController.size(); i++) {
            Object arreglo[] = new Object[4];
            arreglo[0] = pController.get(i).getCodigo();
            arreglo[1] = pController.get(i).getNombre();
            arreglo[2] = pController.get(i).getPrecio();
            arreglo[3] = pController.get(i).getStock();

            dtm.addRow(arreglo);
        }
        this.tProducto.setModel(dtm);
    }

