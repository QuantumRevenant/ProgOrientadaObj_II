    private void addNumberButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNumberButtonActionPerformed
        // TODO add your handling code here:
        int numero;
        ArrayList divisores = new ArrayList();
        numero = (int) Double.parseDouble(numeroOperar.getText());
        
        Operaciones_Ejercicio_3 op = new Operaciones_Ejercicio_3(numero);
        
        divisores = op.getDivisores();
        
        resultado.append(numero + " = {");
        for(int i=0; i<divisores.size() ; i++){
            if(i != divisores.size() - 1){
                resultado.append(divisores.get(i) + "; ");
            }else{
                resultado.append(divisores.get(i) + "}\n");
            }
        }
    }//GEN-LAST:event_addNumberButtonActionPerformed

